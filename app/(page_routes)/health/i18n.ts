const timeframeKeys: Record<Timeframe, keyof typeof translations.en> = {
  minute: "timeframeMinute",
  hour: "timeframeHour",
  day: "timeframeDay",
  week: "timeframeWeek",
  month: "timeframeMonth",
  all: "timeframeAll",
};

export function t(language: Language, key: keyof typeof translations.en, params: TranslationParams = {}) {
  return Object.entries(params).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    translations[language][key]
  );
}

export function getTimeframeLabel(language: Language, timeframe: Timeframe) {
  return t(language, timeframeKeys[timeframe]);
}
